// Player rating mechanics
// ──────────────────────────────────────────────────────

/**
 * Update player's seller rating from a single completed task. Uses an
 * exponential moving average so one bad review doesn't tank the rating
 * permanently.
 */
export function updateRating(currentRating, taskRating, completedCount) {
  const weight = Math.min(0.3, 1 / Math.max(1, completedCount));
  return Math.round((currentRating * (1 - weight) + taskRating * weight) * 10) / 10;
}


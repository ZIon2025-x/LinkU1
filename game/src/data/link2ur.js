/**
 * Build a fresh weekly task board by sampling from templates.
 * Returns 4-6 task instances with light reward jitter and randomized titles.
 *
 * If `opts.state` is provided, may inject one "friend task" (Aditi/林南/Mark)
 * at the top when conditions are met — see link2urFriends.js.
 */
export function generateBoard(week, opts = {}) {
  const rng = opts.rng || Math.random;
  const eligible = LINK2UR_ACCEPT_TEMPLATES.filter(
    (t) => week >= (t.minWeek || 1) && week <= (t.maxWeek || 99),
  );
  const count = 4 + Math.floor(rng() * 3); // 4-6
  const shuffled = [...eligible].sort(() => rng() - 0.5);
  const board = shuffled.slice(0, count).map((t, i) => spawnTask(t, week, rng, i));

  // 30% chance per refresh to surface a friend task at the top of the board —
  // keeps them rare enough to feel discovered rather than weekly chores.
  if (opts.state && rng() < 0.3) {
    const friend = pickFriendTaskForBoard(opts.state, rng);
    if (friend) board.unshift(friend);
  }
  return board;
}

function spawnTask(template, week, rng, idx) {
  const reward = Math.round(
    template.rewardMin + rng() * (template.rewardMax - template.rewardMin),
  );
  const title = template.titles[Math.floor(rng() * template.titles.length)];
  return {
    id: `${template.id}-w${week}-${idx}`,
    templateId: template.id,
    type: template.type,
    emoji: template.emoji,
    title,
    desc: template.desc,
    reward,
    energyCost: template.energyCost,
    actionCost: template.actionCost,
    rating: template.rating,
    week,
  };
}

/**
 * Filter post templates to those available now (week + flag conditions).
 */
export function availablePosts(state) {
  const week = Math.ceil(state.day / 7);
  return LINK2UR_POST_TEMPLATES.filter((p) => {
    if (p.minWeek && week < p.minWeek) return false;
    if (p.maxWeek && week > p.maxWeek) return false;
    if (p.requiresFlag && !state.flags?.[p.requiresFlag]) return false;
    return true;
  });
}


def set_cn(run, size=11, bold=False, color=None, font='宋体'):
    run.font.name = font
    run._element.rPr.rFonts.set(qn('w:eastAsia'), font)
    run.font.size = Pt(size)
    run.font.bold = bold
    if color:
        run.font.color.rgb = color


def cover_title(text, size=22):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    set_cn(run, size=size, bold=True, font='黑体')


def h1(text):
    p = doc.add_paragraph()
